fun main() {
  println("Hello World")
  var name = "John"
  val birthyear = 1975
  println(name)          // Print the value of name
  println(birthyear)     // Print the value of birthyear
  val myNum = 5// Int
  println(myNum)
  val myDoubleNum = 5.99    // Double
  println(myDoubleNum)
  val myLetter = 'D'        // Char
  println(myLetter)
  val myBoolean = true      // Boolean
  println(myBoolean)
  val myText = "Hello"      // String
  println(myText)
}
